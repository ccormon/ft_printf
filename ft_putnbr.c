#include "ft_printf.h"

void	ft_putnbr(unsigned int n)
{
	if (n < 10)
	{
		ft_putchar('0' + n);
		return ;
	}
	ft_putnbr(n / 10);
	ft_putchar('0' + n % 10);
}
